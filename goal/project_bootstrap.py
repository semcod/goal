def _new_bootstrap_result(project_dir: Path, project_type: str) -> Dict:
    return {
        "project_type": project_type,
        "project_dir": project_dir,
        "env_ok": False,
        "tests_found": [],
        "test_created": None,
        "doctor_report": None,
    }


def _pfix_auto_apply(project_dir: Path) -> bool:
    """Return the project's ``[tool.pfix] auto_apply`` setting.

    Defaults to ``True`` when pyproject.toml, the ``[tool.pfix]`` section, or the
    key is absent, so existing projects keep their behaviour. An explicit
    ``auto_apply = false`` lets a project opt out of automatic fixing/applying
    even in ``--all``/``--yes`` mode.
    """
    pyproject = Path(project_dir) / "pyproject.toml"
    if not pyproject.exists():
        return True
    tomllib = get_tomllib()
    if tomllib is None:
        return True
    try:
        content = pyproject.read_text(encoding="utf-8")
        data = tomllib.loads(content) if hasattr(tomllib, "loads") else None
        if data is None:
            with open(pyproject, "rb") as f:
                data = tomllib.load(f)
    except Exception:  # noqa: BLE001 - never let config parsing break the run
        return True
    pfix = (data.get("tool") or {}).get("pfix") or {}
    return bool(pfix.get("auto_apply", True))


def _coerce_bool(value) -> Optional[bool]:
    if isinstance(value, bool):
        return value
    if isinstance(value, str):
        v = value.strip().lower()
        if v in {"1", "true", "yes", "on"}:
            return True
        if v in {"0", "false", "no", "off"}:
            return False
    return None


def _goal_yaml_auto_apply(project_dir: Path) -> Optional[bool]:
    """Read an auto-apply toggle from goal.yaml (works for any project type).

    Looks for top-level ``auto_apply`` / ``auto_fix`` or the same keys under
    ``advanced:``. Returns None when not set or unreadable.
    """
    config = Path(project_dir) / "goal.yaml"
    if not config.exists():
        return None
    try:
        import yaml  # lazy: goal already depends on pyyaml

        data = yaml.safe_load(config.read_text(encoding="utf-8")) or {}
    except Exception:  # noqa: BLE001 - config issues must not break the run
        return None
    if not isinstance(data, dict):
        return None
    for key in ("auto_apply", "auto_fix"):
        if key in data:
            return _coerce_bool(data[key])
    advanced = data.get("advanced")
    if isinstance(advanced, dict):
        for key in ("auto_apply", "auto_fix"):
            if key in advanced:
                return _coerce_bool(advanced[key])
    return None


def _auto_fix_enabled(project_dir: Path) -> bool:
    """Whether automatic fixing/applying is allowed for this project.

    Priority: ``GOAL_AUTO_FIX`` env var, then ``goal.yaml`` (project-type
    agnostic), then ``[tool.pfix] auto_apply`` in pyproject.toml. Defaults to
    True so existing projects are unaffected.
    """
    env = _coerce_bool(os.environ.get("GOAL_AUTO_FIX", ""))
    if env is not None:
        return env
    yaml_setting = _goal_yaml_auto_apply(project_dir)
    if yaml_setting is not None:
        return yaml_setting
    return _pfix_auto_apply(project_dir)


def _run_bootstrap_diagnostics(project_dir: Path, project_type: str, yes: bool):
    # Respect project settings even in --all/--yes mode: GOAL_AUTO_FIX env,
    # goal.yaml auto_apply/auto_fix, or [tool.pfix] auto_apply can opt out.
    auto_fix = yes and _auto_fix_enabled(project_dir)
    if yes and not auto_fix:
        click.echo(
            click.style(
                "  [tool.pfix] auto_apply = false -> skipping auto-fix (report only)",
                fg="yellow",
            )
        )
    click.echo(click.style(f"  DEBUG: auto_fix={auto_fix}", fg="magenta"))
    return diagnose_and_report(project_dir, project_type, auto_fix=auto_fix)


def _ensure_bootstrap_tests(
    project_dir: Path, project_type: str, yes: bool
) -> tuple[List[Path], Optional[Path]]:
    tests_found = find_existing_tests(project_dir, project_type)
    if tests_found:
        return tests_found, None

    test_created = scaffold_test(project_dir, project_type, yes=yes)
    if test_created:
        return [test_created], test_created
    return [], None


def bootstrap_project(project_dir: Path, project_type: str, yes: bool = False) -> Dict:
    """Full bootstrap: diagnose & fix config, ensure environment, scaffold tests.

    Returns a dict with status info:
        {
            'project_type': str,
            'project_dir': Path,
            'env_ok': bool,
            'tests_found': list[Path],
            'test_created': Path | None,
            'doctor_report': DoctorReport | None,
        }
    """
    result = _new_bootstrap_result(project_dir, project_type)

    # Step 1: Diagnose and auto-fix project configuration issues
    result["doctor_report"] = _run_bootstrap_diagnostics(project_dir, project_type, yes)

    # Step 1b: Ensure pfix is installed for auto-fixing errors
    _ensure_pfix_installed(project_dir, yes=yes)

    # Step 2: Ensure environment (venv, deps)
    # Note: For Python projects, this also installs the costs package
    result["env_ok"] = ensure_project_environment(project_dir, project_type, yes=yes)

    # Step 3: Find or scaffold tests
    result["tests_found"], result["test_created"] = _ensure_bootstrap_tests(
        project_dir, project_type, yes
    )

    return result


def bootstrap_all_projects(
    root: Optional[Path] = None, yes: bool = False
) -> List[Dict]:
    """Detect all project types (root + 1-level subfolders) and bootstrap each.

    Returns a list of bootstrap result dicts.
    """
    detected = detect_project_types_deep(root)
    if not detected:
        return []

    results = []
    for ptype, dirs in detected.items():
        for project_dir in dirs:
            try:
                rel = project_dir.relative_to(
                    root.resolve() if root else Path(".").resolve()
                )
            except ValueError:
                rel = project_dir
            click.echo(
                click.style(
                    f"\n📦 Bootstrapping {ptype} project in {rel}", fg="cyan", bold=True
                )
            )
            res = bootstrap_project(project_dir, ptype, yes=yes)
            results.append(res)

    return results


