def _ensure_pfix_env(project_dir: Path) -> bool:
    """Ensure .env file exists with pfix API key template.

    Returns True if .env exists or was created.
    """
    env_file = project_dir / ".env"
    env_example = project_dir / ".env.example"

    # Template content for pfix .env
    env_template = """# pfix Configuration
# Self-healing Python — auto-fixes runtime errors via LLM

# Required: OpenRouter API key (https://openrouter.ai/keys)
OPENROUTER_API_KEY=

# Model (default: openrouter/qwen/qwen3-coder-next)
LLM_MODEL=openrouter/qwen/qwen3-coder-next

# Behavior
PFIX_AUTO_APPLY=true         # true = apply fixes without asking
PFIX_AUTO_INSTALL_DEPS=true   # true = auto pip/uv install
PFIX_AUTO_RESTART=false        # true = os.execv restart after fix
PFIX_MAX_RETRIES=3
PFIX_DRY_RUN=false
PFIX_ENABLED=true

# Git integration
PFIX_GIT_COMMIT=false         # true = auto-commit fixes
PFIX_GIT_PREFIX=pfix:         # commit message prefix

# Backup
PFIX_CREATE_BACKUPS=false     # false = disable .pfix_backups/ directory
"""

    _, existing_api_key = _find_openrouter_api_key(project_dir)
    if existing_api_key:
        return True

    created = False

    try:
        # Create .env from template if not exists
        if not env_file.exists():
            env_file.write_text(env_template, encoding="utf-8")
            click.echo(
                click.style(
                    "  ✓ Created .env template (add your OPENROUTER_API_KEY)",
                    fg="green",
                )
            )
            created = True

        # Also create .env.example (for git)
        if not env_example.exists():
            env_example.write_text(env_template, encoding="utf-8")

        # Ensure .env is in .gitignore
        gitignore = project_dir / ".gitignore"
        if gitignore.exists():
            gitignore_content = gitignore.read_text(encoding="utf-8")
            if ".env" not in gitignore_content:
                with open(gitignore, "a", encoding="utf-8") as f:
                    f.write("\n# Environment variables\n.env\n")
        else:
            gitignore.write_text(".env\n.env.local\n", encoding="utf-8")

        return True
    except OSError as e:
        logger.warning("Failed to create pfix env template in %s: %s", project_dir, e)
        click.echo(click.style(f"  ⚠ Could not create pfix .env: {e}", fg="yellow"))
        return False


def _validate_pfix_env(project_dir: Path) -> bool:
    """Validate that OPENROUTER_API_KEY is configured in .env.

    Shows error message if key is missing or empty.
    Returns True if key is present, False otherwise.
    """
    env_file, api_key = _find_openrouter_api_key(project_dir)
    if not env_file:
        env_file = project_dir / ".env"

    if not api_key:
        click.echo(
            click.style("\n  ⚠️ OPENROUTER_API_KEY not configured!", fg="red", bold=True)
        )
        click.echo(
            click.style(
                "  pfix requires an OpenRouter API key to fix errors.", fg="yellow"
            )
        )
        click.echo(click.style("\n  To fix this:", fg="cyan"))
        click.echo(
            click.style(
                "  1. Get your API key from: https://openrouter.ai/keys", fg="yellow"
            )
        )
        click.echo(click.style(f"  2. Add it to: {env_file}", fg="yellow"))
        click.echo(click.style("  3. Restart goal -a\n", fg="yellow"))
        return False

    # Check if key looks valid (starts with sk-or-v1-)
    if not api_key.startswith("sk-or-v1-"):
        click.echo(
            click.style(
                "\n  ⚠️ OPENROUTER_API_KEY format appears invalid!", fg="red", bold=True
            )
        )
        click.echo(click.style("  Expected format: sk-or-v1-...", fg="yellow"))
        click.echo(
            click.style(
                "\n  Please verify your API key at: https://openrouter.ai/keys",
                fg="cyan",
            )
        )
        return False

    click.echo(click.style("  ✓ OPENROUTER_API_KEY configured", fg="green"))
    return True


def _ensure_pfix_installed(project_dir: Path, yes: bool = False) -> bool:
    """Ensure pfix package is installed for auto-fixing errors.

    By default installs from PyPI. For local development, set
    GOAL_PFIX_LOCAL_PATH to install from a specific editable path.

    Returns True if pfix is ready to use.
    """
    python_bin = _find_python_bin(project_dir)

    # Check if pfix is already installed
    result = subprocess.run(
        [python_bin, "-c", "import pfix; print(pfix.__version__)"],
        capture_output=True,
        text=True,
        cwd=str(project_dir),
    )

    if result.returncode != 0:
        local_pfix_path = os.environ.get("GOAL_PFIX_LOCAL_PATH", "").strip()
        if local_pfix_path:
            local_pfix = Path(local_pfix_path).expanduser().resolve()
            if local_pfix.exists():
                click.echo(
                    click.style(
                        "  Installing pfix from configured local path...", fg="cyan"
                    )
                )
                install_cmd = [
                    python_bin,
                    "-m",
                    "pip",
                    "install",
                    "-e",
                    str(local_pfix),
                ]
            else:
                click.echo(
                    click.style(
                        f"  ⚠ GOAL_PFIX_LOCAL_PATH does not exist: {local_pfix}. Falling back to PyPI.",
                        fg="yellow",
                    )
                )
                click.echo(click.style("  Installing pfix from PyPI...", fg="cyan"))
                install_cmd = [python_bin, "-m", "pip", "install", "pfix>=0.1.60"]
        else:
            click.echo(click.style("  Installing pfix from PyPI...", fg="cyan"))
            install_cmd = [python_bin, "-m", "pip", "install", "pfix>=0.1.60"]

        install_result = subprocess.run(
            install_cmd, capture_output=True, text=True, cwd=str(project_dir)
        )
        if install_result.returncode != 0:
            click.echo(click.style("  ⚠ Could not install pfix package", fg="yellow"))
            return False
        click.echo(click.style("  ✓ Pfix package installed", fg="green"))
    else:
        click.echo(
            click.style(
                f"  ✓ Pfix package already installed ({result.stdout.strip()})",
                fg="green",
            )
        )

    # Check/add pfix config to pyproject.toml
    _ensure_pfix_config(project_dir, yes=yes)

    # Create .env template for API key if not exists
    env_ok = _ensure_pfix_env(project_dir)

    # Validate API key is configured
    if env_ok:
        _validate_pfix_env(project_dir)

    return True


def _ensure_pfix_config(project_dir: Path, yes: bool = False) -> bool:
    """Ensure [tool.pfix] section exists in pyproject.toml.

    Returns True if config was added or already exists.
    """
    pyproject = project_dir / "pyproject.toml"
    if not pyproject.exists():
        return False

    content = pyproject.read_text(encoding="utf-8")

    # Check if already configured
    if "[tool.pfix]" in content:
        return True

    # Add pfix configuration
    pfix_config = """\n[tool.pfix]
# Self-healing Python configuration
model = "openrouter/qwen/qwen3-coder-next"
auto_apply = true
auto_install_deps = true
auto_restart = false
max_retries = 3
create_backups = false
git_auto_commit = false

[tool.pfix.runtime_todo]
enabled = true
todo_file = "TODO.md"
min_severity = "low"
deduplicate = true
"""

    try:
        with open(pyproject, "a", encoding="utf-8") as f:
            f.write(pfix_config)
        click.echo(click.style("  ✓ Added [tool.pfix] to pyproject.toml", fg="green"))
        return True
    except OSError as e:
        logger.warning("Failed to append [tool.pfix] config to %s: %s", pyproject, e)
        click.echo(click.style(f"  ⚠ Could not add pfix config: {e}", fg="yellow"))
        return False


